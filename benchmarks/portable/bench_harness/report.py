def _load_rows(raw_csv: Path) -> list[dict]:
    with raw_csv.open("r", newline="") as fh:
        return list(csv.DictReader(fh))


def _system_meta(systems: list[str]) -> dict[str, dict[str, str]]:
    out: dict[str, dict[str, str]] = {}
    for system in systems:
        try:
            manifest = AdapterManifest.load(Path(__file__).resolve().parent.parent / f"{system}-bench")
            out[system] = {
                "family": manifest.family,
                "display_name": manifest.display_name,
            }
        except Exception:
            out[system] = {"family": system, "display_name": system}
    return out


def _timeline_series(rows: list[dict], systems: list[str]) -> dict[str, dict[str, list[dict]]]:
    """Aggregate raw rows into per-metric, per-system time series."""
    out: dict[str, dict[str, list[dict]]] = {}
    for metric in DEFAULT_TIMELINE_METRICS:
        out[metric] = {}
        for system in systems:
            selected = [
                r
                for r in rows
                if r["system"] == system and r["metric"] == metric
            ]
            if not selected:
                continue

            # Adapter metrics are one value per tick. Table metrics such as
            # n_dead_tup need summing across subjects for each timestamp.
            if metric == "n_dead_tup":
                per_elapsed: dict[tuple[float, str, str], float] = {}
                for row in selected:
                    if row["subject_kind"] != "table":
                        continue
                    key = (
                        float(row["elapsed_s"]),
                        row["phase_label"],
                        row["phase_type"],
                    )
                    per_elapsed[key] = per_elapsed.get(key, 0.0) + float(row["value"])
                points = [
                    {
                        "elapsed_s": elapsed,
                        "phase_label": phase_label,
                        "phase_type": phase_type,
                        "value": value,
                    }
                    for (elapsed, phase_label, phase_type), value in sorted(per_elapsed.items())
                ]
            else:
                points = [
                    {
                        "elapsed_s": float(row["elapsed_s"]),
                        "phase_label": row["phase_label"],
                        "phase_type": row["phase_type"],
                        "value": float(row["value"]),
                    }
                    for row in selected
                    if row["subject_kind"] == "adapter"
                ]
                points.sort(key=lambda item: item["elapsed_s"])

            if points:
                out[metric][system] = points
    return out


def _phase_table(summary: dict, systems: list[str], phases: list[Phase], system_meta: dict[str, dict[str, str]]) -> str:
    headers = [
        "System",
        "Phase",
        "Type",
        "Throughput/s",
        "Producer p99 ms",
        "Subscriber p99 ms",
        "End-to-end p99 ms",
        "Median dead tuples",
        "Peak dead tuples",
    ]
    rows: list[str] = []
    for system in systems:
        system_block = summary["systems"].get(system, {})
        phase_map = system_block.get("phases", {})
        for phase in phases:
            block = phase_map.get(phase.label)
            if not block:
                continue
            rows.append(
                "<tr>"
                f"<td>{html.escape(system_meta[system]['display_name'])}</td>"
                f"<td>{html.escape(phase.label)}</td>"
                f"<td>{html.escape(block.get('phase_type', ''))}</td>"
                f"<td data-sort='{block.get('median_throughput_per_s') or 0}'>{(block.get('median_throughput_per_s') or 0):.1f}</td>"
                f"<td data-sort='{block.get('median_producer_p99_ms') or 0}'>{(block.get('median_producer_p99_ms') or 0):.3f}</td>"
                f"<td data-sort='{block.get('median_subscriber_p99_ms') or 0}'>{(block.get('median_subscriber_p99_ms') or 0):.3f}</td>"
                f"<td data-sort='{block.get('median_end_to_end_p99_ms') or 0}'>{(block.get('median_end_to_end_p99_ms') or 0):.3f}</td>"
                f"<td data-sort='{block.get('median_dead_tup') or 0}'>{(block.get('median_dead_tup') or 0):.0f}</td>"
                f"<td data-sort='{block.get('peak_dead_tup') or 0}'>{(block.get('peak_dead_tup') or 0):.0f}</td>"
                "</tr>"
            )
    head = "".join(f"<th data-sortable='true'>{html.escape(h)}</th>" for h in headers)
    return (
        "<table id='phase-summary-table'>"
        f"<thead><tr>{head}</tr></thead>"
        f"<tbody>{''.join(rows)}</tbody>"
        "</table>"
    )


def _plot_gallery(run_dir: Path) -> str:
    cards: list[str] = []
    for stem in PLOT_ORDER:
        svg = run_dir / "plots" / f"{stem}.svg"
        if not svg.exists():
            continue
        title = stem.replace("_", " ").title()
        rel = f"plots/{svg.name}"
        cards.append(
            "<button class='plot-card' data-plot-src='{src}' data-plot-title='{title}'>"
            "<div class='plot-card-title'>{title}</div>"
            "<img src='{src}' alt='{title}' loading='lazy' />"
            "</button>".format(src=html.escape(rel), title=html.escape(title))
        )
    return "<div class='plot-grid'>" + "".join(cards) + "</div>"


