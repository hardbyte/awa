def write_interactive_report(
    *,
    run_dir: Path,
    raw_csv: Path,
    summary: dict,
    manifest: dict,
    phases: list[Phase],
    systems: list[str],
) -> Path:
    rows = _load_rows(raw_csv)
    system_meta = _system_meta(systems)
    timeline_data = _timeline_series(rows, systems)
    html_body = _build_html(
        run_id=manifest["run_id"],
        systems=systems,
        phases=phases,
        manifest=manifest,
        summary=summary,
        timeline_data=timeline_data,
        system_meta=system_meta,
        run_dir=run_dir,
    )
    path = run_dir / "index.html"
    path.write_text(html_body)
    return path
